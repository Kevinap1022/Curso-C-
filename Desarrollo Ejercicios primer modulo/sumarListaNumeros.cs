using System;
using System.Linq;

namespace PracticaCursoSichar
{
    class Program
    {
        static void Main(string[] args)
        {
            int[] listaNumeros = { 0, 1, 2, 3, 4, 54, 65, 76 };
            int resultado = sumarListaNumeros(listaNumeros);
            Console.WriteLine(" La suma total de la lista de numeros es  " + resultado);
            Console.Read();

        }

        static int sumarListaNumeros(int[] listaNumeros)
        {
            int totalSuma = listaNumeros.Sum();
            return totalSuma;
        }
    }
}